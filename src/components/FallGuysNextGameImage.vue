<script lang="ts">
import {
  defineComponent,
  ref,
  nextTick,
  onMounted,
  watch,
  computed,
} from "vue";

import { useStore } from "../store";

const DEFAULT_SIZE_OF_TITLE = 50;

export default defineComponent({
  name: "FallGuysNextGameImage",
  setup(props) {
    const { readonlyState: state, typeColor, uploadImage } = useStore();

    // width of title
    const textOfTitle = ref<SVGTextElement>();
    const widthOfTitle = ref(DEFAULT_SIZE_OF_TITLE);
    const updateWidthOfTitle = () => {
      nextTick(() => {
        if (state.title === "") {
          widthOfTitle.value = DEFAULT_SIZE_OF_TITLE;
        } else {
          widthOfTitle.value = textOfTitle.value!.clientWidth;
        }
      });
    };
    onMounted(updateWidthOfTitle);
    watch(() => state.title, updateWidthOfTitle);

    // lines of how to play
    const howToPlayLines = computed(() => state.howToPlay.split("\n"));

    return {
      state,
      typeColor,
      uploadImage,

      // width of title
      textOfTitle,
      widthOfTitle,

      // lines of how to play
      howToPlayLines,
    };
  },
});
